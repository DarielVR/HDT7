public class LangDic {
    
    String spanish;
    String english;
    String french;

    public LangDic (String english, String spanish, String french) {
        this.spanish = spanish;
        this.english = english;
        this.french = french;
    }

    public String getSpanish() {
        return spanish;
    }

    public String getEnglish() {
        return english;
    }

    public String getFrench() {
        return french;
    }

}
